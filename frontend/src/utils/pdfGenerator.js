  rr(doc,MARGIN.left,y-1,CONTENT_W,10,2,BRAND.primary);
  doc.setFont('helvetica','bold'); doc.setFontSize(9); doc.setTextColor(...rgb(BRAND.white));
  doc.text('DAY',MARGIN.left+4,y+5); doc.text('HOURS',MARGIN.left+50,y+5); doc.text('TASKS',MARGIN.left+95,y+5); doc.text('SCORE',MARGIN.left+135,y+5); y+=12;
