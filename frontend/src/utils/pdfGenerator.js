  doc.setFont('helvetica','normal'); doc.setFontSize(9); doc.setTextColor(...rgb(BRAND.muted));
  doc.text(`${user?.email||''} · ${user?.college||''} · ${user?.course||''} · ${user?.year||''}`,MARGIN.left+8,y+20);
  y+=34;

  // Stats cards
  const sCards=[{l:'Total XP',v:`${stats?.xp||0}`,c:BRAND.primary},{l:'Day Streak',v:`${stats?.streak||0}`,c:BRAND.secondary},{l:'Level',v:`${stats?.level||1}`,c:BRAND.warning},{l:'Badges',v:`${Object.keys(stats?.badges||{}).length}`,c:BRAND.success}];
